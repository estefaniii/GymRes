-- 5. TRIGGER: Auto-create or Update profile from metadata
-- This ensures that even if a user is deleted and recreated, the data syncs correctly.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
    INSERT INTO public.usuarios (
        id, 
        email, 
        nombre, 
        apellido, 
        rol, 
        apartamento, 
        genero, 
        rango_edad, 
        acepta_marketing
    )
    VALUES (
        new.id, 
        new.email, 
        COALESCE(new.raw_user_meta_data->>'nombre', ''),
        COALESCE(new.raw_user_meta_data->>'apellido', ''),
        COALESCE(new.raw_user_meta_data->>'rol', 'inquilino'),
        new.raw_user_meta_data->>'apartamento',
        new.raw_user_meta_data->>'genero',
        new.raw_user_meta_data->>'rango_edad',
        COALESCE((new.raw_user_meta_data->>'acepta_marketing')::boolean, true)
    )
    ON CONFLICT (id) DO UPDATE SET
        email = EXCLUDED.email,
        nombre = EXCLUDED.nombre,
        apellido = EXCLUDED.apellido,
        rol = EXCLUDED.rol,
        apartamento = EXCLUDED.apartamento,
        genero = EXCLUDED.genero,
        rango_edad = EXCLUDED.rango_edad,
        acepta_marketing = EXCLUDED.acepta_marketing;
    RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

