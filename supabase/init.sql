-- 1. TABLES: Unified Structure
CREATE TABLE IF NOT EXISTS public.usuarios (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email TEXT UNIQUE NOT NULL,
    nombre TEXT DEFAULT '',
    apellido TEXT DEFAULT '',
    genero TEXT,
    rango_edad TEXT,
    rol TEXT DEFAULT 'inquilino', -- 'admin', 'inquilino', 'propietario', 'huésped'
    apartamento TEXT,
    acepta_marketing BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.reservas (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES public.usuarios(id) ON DELETE CASCADE NOT NULL,
    user_name TEXT DEFAULT 'Usuario',
    date DATE NOT NULL,
    start_hour INTEGER NOT NULL,
    duration INTEGER DEFAULT 1,
    status TEXT DEFAULT 'activa',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.anuncios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    image_url TEXT NOT NULL,
    link_url TEXT,
    title TEXT DEFAULT 'Nuevos equipos disponibles',
    description TEXT DEFAULT 'Conoce las novedades del gimnasio',
    active BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. SECURITY DEFINER: Safe role checking
CREATE OR REPLACE FUNCTION public.is_admin(user_uuid UUID)
RETURNS BOOLEAN AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1 FROM public.usuarios 
        WHERE id = user_uuid AND rol = 'admin'
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

